export const mutations = {
  GET_PORTFOLIOS: (state, datas) => (state.portfolios = datas),
  MODIFY_CATEGORY: (state, datas) => (state.categories = datas),
  GET_PORTFOLIO: (state, data) => {
    data.images.push(data.image)
    state.portfolio = data
  },
};
export const actions = {
  getPortfolios({ commit, state }, id="") {
    if (id) {
      let mainApi = [...state.main_api];
      let filteredData = mainApi.filter(el => {
        if (el.category_ids.includes(id)) {
          return el;
          }
      })
      commit("GET_PORTFOLIOS", filteredData)

      let modifyData = state.categories.map(el => {
          return {
            ...el,
            isActive: false
          }
      }).map(e => {
         if (e.id === id) {

          return {
            ...e,
            isActive: true
          }
        }
        return e;
      })

    commit("MODIFY_CATEGORY", modifyData)
    } else {
       commit("GET_PORTFOLIOS", state.main_api)
    }
  },
  getPortfolioBySlug({ commit, state }, slug) {
    if (slug) {
      let mainApi = [...state.main_api];
      let filteredData = mainApi.find(el => el.slug === slug)

      if (filteredData) {
        commit("GET_PORTFOLIO", filteredData)
      } else {
        return false;
      }
    } else {
      return false;
    }
  },

};
